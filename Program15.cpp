#include <iostream>
using namespace std;

int main() {
  int x = 5;
  int y = 3;

  // Arithematic Operators:
  
  cout << x + y << "\n";
  cout << x - y << "\n";
  cout << x * y << "\n";
  cout << x / y << "\n";
  cout << x % y << "\n";
  ++x;
  cout << x << "\n";
  --y;
  cout << y << "\n";

  return 0;
}
