#include<iostream>
using namespace std;

// Assignment Operators

int main(){
      int x = 5;
      cout << x << "\n";
      
      x+=3;
      cout << x << "\n";

      x-=3;
      cout << x << "\n";

      x*=3;
      cout << x << "\n";

      x/=3;
      cout << x << "\n";

      x%=3;
      cout << x << "\n";

      x&=3;
      cout << x << "\n";

      x|=3;
      cout << x << "\n";

      x^=3;
      cout << x << "\n";

      x>>=3;
      cout << x << "\n";

      x<<=3;
      cout << x << "\n";

    return 0;
}